// Mon은 Monster.
public class CMonInfo
{
    public int m_nId;
    public int m_nHp;
    public int m_nSpeed;
    public int m_nAttack;
    public int m_nCritical;

  public CMonInfo(int nId, int nHp, int nSpeed,int nAttack, int nCritcial)
    {

        this.m_nId = nId;
        this.m_nHp = nHp;
        this.m_nSpeed = nSpeed;
        this.m_nAttack = nAttack;
        this.m_nCritical = nCritcial;

    }
}

