public class CLevelInfo
{
    public int m_nId;
    public int m_nLv;
    public int m_nHp;
    public int m_nAttack;
    public int m_nCritical;

    public CLevelInfo(int nId, int nLv, int nHp, int nAttack, int nCritical)
    {
        this.m_nId = nId;
        this.m_nLv = nLv;
        this.m_nHp = nHp;
        this.m_nAttack = nAttack;
        this.m_nCritical = nCritical;
    }

}
